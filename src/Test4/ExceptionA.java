package Test4;

public class ExceptionA {

    public void doA(int k) throws Exception {
        for(int i = 0; i < 10; i++) {
            if(i == k) throw new Exception("Index of k is " + i);
        }
    }

    public void doB(boolean f) throws Exception {               // Throws Exception is a must here because doB() is calling doA() so it needs to handle the Exception that is generated by the doA method.
        if(f) {
            doA(15);
        }
    }

    public static void main(String[] args) throws Exception {       // The main() method is calling doB(), which generates an exception (due to the above reason). Therefore main() must also declare that it's throws Exception or wrap the call to doB() in a try block.

        ExceptionA a = new ExceptionA();
        a.doB(args.length> 0);
    }
}
